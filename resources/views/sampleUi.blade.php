              <button class="accordion-button collapsed" type="button" data-bs-toggle="collapse" data-bs-target="#faq-item-2" aria-expanded="false">
                  How can I take the BukSU College Admission Test?
              </button>
            </h2>
            <div id="faq-item-2" class="accordion-collapse collapse" data-bs-parent="#accordion-faq" style="">
              <div class="accordion-body">
                <p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-family: arial, helvetica, sans-serif; color: black;">Follow these steps to apply for the BukSU College Admission Test:</span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;">&nbsp;</p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-family: arial, helvetica, sans-serif; color: black;">Step 1. Access this link <a href="https://admissions.buksu.edu.ph/">http://admissions.buksu.edu.ph</a>. Register using your active Gmail account. Create your password and re-type the password for verification.</span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-family: arial, helvetica, sans-serif; color: black;">&nbsp;</span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-family: arial, helvetica, sans-serif; color: black;">Step 2. After creating an account, an email containing a confirmation link will be sent to your Gmail. Check your Gmail inbox and click the confirmation link to activate your account. </span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;">&nbsp;</p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-weight: 400; font-family: arial, helvetica, sans-serif;">Step 3: Fill-out all the necessary information and upload the documentary requirements.</span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;">&nbsp;</p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-weight: 400; font-family: arial, helvetica, sans-serif;">Step 4: Select your preferred schedule (venue, date and time) based on the availability of slots.</span></p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;">&nbsp;</p>
<p style="vertical-align: baseline; margin: 0in 0in .0001pt .5in;"><span style="font-weight: 400; font-family: arial, helvetica, sans-serif;">Step 5: You will get notifications when your application to take the entrance exam on your preferred date and venue is accepted. Once accepted, download and print your Test Permit.</span></p>
              </div>
            </div>
        </div>
    
